package co.huby.prj.code.service;

public class CodeVo {

	private String code_id;
	private String code_part;
	private String code_name;
	private String code_use;

	public CodeVo() {

	}

	public String getCode_id() {
		return code_id;
	}

	public void setCode_id(String code_id) {
		this.code_id = code_id;
	}

	public String getCode_part() {
		return code_part;
	}

	public void setCode_part(String code_part) {
		this.code_part = code_part;
	}

	public String getCode_name() {
		return code_name;
	}

	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}

	public String getCode_use() {
		return code_use;
	}

	public void setCode_use(String code_use) {
		this.code_use = code_use;
	}

}
